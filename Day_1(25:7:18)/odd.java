import java.util.*;
    /* Accept a number print wether odd or even*/

    class oddsum {
        public static void main(String[] args) {
            Scanner sc = new Scanner(System.in);
            int a = sc.nextInt();
            if (a % 2 == 0) {
                System.out.print("even");
            }
            else {
                System.out.print("odd");
            }


        }
    }
